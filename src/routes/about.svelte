	<div class="row">
		<div class="col-6">
			<h5>
				Global Archives Online is designed to identify and locate open resources for the study of
				and research on colonial and global history. Aimed primarily at students and researchers
				interested in imperial, colonial and global history, the directory provides users anywhere
				in the world with an overview of major digital collections of a variety of primary sources,
				including digitized manuscripts, newspapers, books, pamphlets, maps, images and audio-visual
				material. The user-friendly interface of Global Archives Online enables a systematic search
				and helps to organize digital research. In addition, it offers an efficient internal search
				function and provides useful insider information. All resources included in the directory
				are open access.
			</h5>
			<h5>
				The purpose of Global Archives Online is to facilitate research and study in the fields of
				imperial, colonial and global history broadly understood. For students and researchers,
				Global Archives Online provides a comprehensive and up-to-date entry to digital sources. It
				serves as an entry point and tool for finding resources in the “deep web” and allows users
				to deepen their understanding of specific local or institutional conditions of archives and
				their collections. Teachers interested in training students in archival research to consult,
				transliterate and interpret primary sources. It encourages students to use archival sources
				in their papers and theses and transcend the limitations of national historiographies and
				collections of historical sources. In doing so, Global Archives Online aims to stimulate
				interest in the fields of imperial, colonial and global history. As a freely available and
				easily accessible research tool, Global Archives Online offers new research opportunities
				for students, researchers, and the general public around the world. A further aim is to
				contribute to narrowing the gap between different research regions inside academia on the
				one hand and between academia and the broader public on the other.
			</h5>
			<h5>
				Global Archives Online is operated by the Research Cluster on Colonial Connections and
				Comparisons, which is part of the Linnaeus University Centre for Concurrences in Colonial
				and Postcolonial Studies. Contributors from the cluster Active contributors include global
				historians working on different parts of the world and their familiarity with different
				types of archives and their digital collections allows them to provide valuable insights
				into the collections and the techniques in using the online resources. An advisory board
				consisting of archivists, global historians and digital humanities experts regularly
				provides critical comments and inspiring ideas on how to keep the directory up to-date with
				developments of open access digital research
			</h5>
